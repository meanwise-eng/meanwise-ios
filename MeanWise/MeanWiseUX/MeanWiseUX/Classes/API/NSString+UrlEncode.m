#import "NSString+UrlEncode.h"

@implementation NSString (NSString_Extended)

- (NSString *)urlencode {
    NSMutableString *output = [NSMutableString string];
    const unsigned char *source = (const unsigned char *)[self UTF8String];
    const unsigned long sourceLen = strlen((const char *)source);
    for (int i = 0; i < sourceLen; ++i) {
        const unsigned char thisChar = source[i];
        if (thisChar == ' '){
            [output appendString:@"+"];
        } else if (thisChar == '.' || thisChar == '-' || thisChar == '_' || thisChar == '~' ||
                   (thisChar >= 'a' && thisChar <= 'z') ||
                   (thisChar >= 'A' && thisChar <= 'Z') ||
                   (thisChar >= '0' && thisChar <= '9')) {
            [output appendFormat:@"%c", thisChar];
        } else {
            [output appendFormat:@"%%%02X", thisChar];
        }
    }
    return output;
}
-(NSString *)urlFilterCharacters
{
 
    NSString *temp=[NSString stringWithFormat:@"%@",self];
    if([temp containsString:@"&"])
    {
        temp=[temp stringByReplacingOccurrencesOfString:@"&" withString:@"%26"];
    }
    if([temp containsString:@" "])
    {
        temp=[temp stringByReplacingOccurrencesOfString:@" " withString:@"%20"];
    }
    
    
    return temp;
}
@end
